#!/usr/bin/env node
/**
 * Postbuild patch: ensures /__manifest is never CDN-cached.
 *
 * When running on Vercel CI, `react-router build` outputs to build/ and Vercel CLI
 * generates .vercel/output/config.json AFTER npm run build completes — so this script
 * only has work to do during LOCAL builds (e.g. `vercel build` run locally, which does
 * write .vercel/output/ as part of the build step).  On Vercel CI, vercel.json already
 * contains the `max-age=0, must-revalidate` override for /__manifest which Vercel CLI
 * applies when it generates the output config post-build.  We skip gracefully when the
 * file is not present so Vercel CI builds don't fail.
 *
 * Root cause context: react-router's Vercel adapter bakes
 * `s-maxage=86400, stale-while-revalidate=86400` into the /__manifest route.
 * React Router fetches /__manifest on every client-side navigation to resolve route →
 * JS chunk mappings.  A stale CDN-cached manifest causes old chunk URLs to be loaded
 * even after a new deploy — and Vercel keeps old assets live, so users silently see
 * old code until they hard-refresh.
 */

import { existsSync, readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";

const configPath = resolve(".vercel/output/config.json");

if (!existsSync(configPath)) {
  // Running on Vercel CI: .vercel/output/ is generated by Vercel CLI after this
  // script exits.  The vercel.json header rule for /__manifest handles the override.
  console.log("[patch-vercel-manifest-cache] .vercel/output/config.json not found — skipping (Vercel CI handles this via vercel.json).");
  process.exit(0);
}

const config = JSON.parse(readFileSync(configPath, "utf8"));

let patched = false;
config.routes = config.routes.map((route) => {
  if (route.src === "^/__manifest$") {
    const old = route.headers?.["Cache-Control"] ?? "(none)";
    route.headers = { ...route.headers, "Cache-Control": "no-store" };
    console.log(`[patch-vercel-manifest-cache] /__manifest Cache-Control: ${old} → no-store`);
    patched = true;
  }
  return route;
});

if (!patched) {
  // Route not found — add it explicitly before the filesystem handler.
  const filesystemIdx = config.routes.findIndex((r) => r.handle === "filesystem");
  const insertAt = filesystemIdx === -1 ? config.routes.length : filesystemIdx;
  config.routes.splice(insertAt, 0, {
    src: "^/__manifest$",
    headers: { "Cache-Control": "no-store" },
    continue: true,
  });
  console.log("[patch-vercel-manifest-cache] /__manifest rule not found — inserted before filesystem handler");
}

writeFileSync(configPath, JSON.stringify(config, null, 2) + "\n");
console.log("[patch-vercel-manifest-cache] Done.");
